package unittest;

/**
 * ユニットテストのサンプルに使用する簡単なクラス。
 *
 * @author naotake
 */
public class Sample {

    public int add(Integer x, Integer y) {
        return x + y;
    }

    public int subtract(Integer x, Integer y) {
        return x - y;
    }

    public boolean equals(Integer x, Integer y) {
        return x.equals(y);
    }
}
